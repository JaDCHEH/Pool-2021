int	ft_is_prime(int	n)
{
	int	i;
	int	j;

	i = 2;
	j = n / 2;
	if (n == 0 || n == 1)
		return (1);
	while (i < j)
	{
		if ((n % i) == 0)
			return (0);
		i++;
	}
	return (1);
}
