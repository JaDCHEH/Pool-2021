int	ft_atoi(char	*str)
{
	int	i;
	int	n;
	int	s;

	i = 0;
	s = 1;
	n = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s = -s;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = str[i] - 48 + (n * 10);
		if (str[i + 1] < '0' || str[i + 1] > '9')
			return (s * n);
		i++;
	}
	return (0);
}
int	main()
{
	char t[]="    +++-+-+------+-+-+--++-ghhy";
	printf("%d", ft_atoi(t));
}