#include<unistd.h>
int	main(int	argc, char	**argv)
{
	int	p;
	int	a;

	a = argc - 1;
	while (a > 0)
	{
		p = 0;
		while(argv[a][p] != '\0')
		{
			write(1, &argv[a][p], 1);
			p++;
		}
		write(1, "\n", 1);
		a--;
	}
}
