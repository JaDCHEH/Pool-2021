char	*dt_strdup(char	*src)
{
	int		i;
	int		j;
	char	*n;

	i = 0;
	while (src[i] != '\0')
		i++;
	n = (char*)malloc(sizeof(char) * i + 1);
	j = 0;
	while (j < i)
	{
		n[j] = src[j];
		j++;
	}
	n[j] = '\0';
	return (n);
}

