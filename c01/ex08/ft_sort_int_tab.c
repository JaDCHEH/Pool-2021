void	ft_sort_int_tab(int	*tab, int	size)
{
	int	a;
	int	t;

	a = 0;
	while (a < size - 1)
	{
		if (tab[a] > tab[a + 1])
		{
			t = tab[a];
			tab[a] = tab[a + 1];
			tab[a + 1] = t;
			a = -1;
		}
		a++;
	}
}
